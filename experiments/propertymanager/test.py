def parseSpread(s: str):
    s = int(s)
    if s == 0:
        return "NORMAL"
    elif s == 1:
        return "RFNORMAL"
    elif s == 2:
        return "RFPU"
    elif s == 3:
        return "RF"
    elif s == 4:
        return "ICEPU"
    elif s == 5:
        return "ICE"
    elif s == 6:
        return "PEPPERSPRAY"
    elif s == 7:
        return "GUN8"
    return "TOASTER"

def parseStyle(s: str):
    s = int(s)
    x = ["NORMAL", "MISSILE", "POPCORN", "CAPPED"]
    return x[s]

